print("----- Rạp Chiếu Phim ABC -----")
print(" 1. Lựa chọn phim tình cảm ")
print(" 2. Lựa chọn phim kinh dị ")
print(" 3. Lựa chọn phim hoạt hình ")
print(" 4. Lựa chọn phim khoa học viễn tưởng")
chon = input(" Chọn thể loại phim bạn muốn xem: ")
if chon == '1':
    print(" Phim tình cảm.")
elif chon == '2':
    print(" Phim kinh dị.")
elif chon == '3':
    print(" Phim hoạt hình.")
elif chon == '4':
    print(" Phim khoa học viễn tưởng.")
else:
    print("Lựa chọn không hợp lệ. Vui lòng nhập lại.")